function drawWalls() {
	for (var i = 0, borderTop = 6; i < map.length; i++, borderTop += box) {  //vertical
		for (var j = 0, borderLeft = box; j < map[i].length; j++, borderLeft += box) {  //horizontal
			if(map[i][j] === 1) {
				ctx.drawImage(imgWall, borderLeft, borderTop);
			}
		}
	}
}

function clearMap(LengthLeft, LengthTop) {
	indexI = myMathRound( (LengthTop - 6)/box );
	indexJ = myMathRound( (LengthLeft-box)/box );

	map[indexI][indexJ] = 0;
}

function myMathRound(num) {
	if(num + 0.5 > Math.ceil(num) ) {
		return Math.ceil(num);
	} 
	else if (num + 0.5 <= Math.ceil(num)) {
		return Math.floor(num);
	}
}